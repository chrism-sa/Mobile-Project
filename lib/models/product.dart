class Product {
  final String name;
  final String description;
  final String image;
  final String price;
  final String unit;
  final double rating;
  final String kategori;

  const Product({
    required this.name,
    required this.description,
    required this.image,
    required this.price,
    required this.unit,
    required this.rating,
    required this.kategori
  });
}
